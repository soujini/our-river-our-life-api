passport.use('id', new BasicStrategy((id, done) => {
  const userSchema = new Schema({ id: schema.tree.id})

  userSchema.validate({ id }, (err) => {
    if (err) done(err)
  })

  User.findOne({ id }).then((user) => {
    if (!user) {
      done(true)
      return null
    }
    return user.authenticate(id, user.id).then((user) => {
      done(null, user)
      return null
    }).catch(done)
  })
}))


passport.use('token', new JwtStrategy({
  secretOrKey: jwtSecret,
  jwtFromRequest: ExtractJwt.fromExtractors([
    ExtractJwt.fromUrlQueryParameter('access_token'),
    ExtractJwt.fromBodyField('access_token'),
    ExtractJwt.fromAuthHeaderWithScheme('Bearer')
  ])
}, ({ id }, done) => {
  User.findById(id).then((user) => {
    done(null, user)
    return null
  }).catch(done)
}))
